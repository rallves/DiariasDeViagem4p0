SELECT *
FROM
(
SELECT NVL(SUM(AD_ADVTRC.VALOR), 0) VLR_TRECHO
FROM AD_ADVTRC
WHERE AD_ADVTRC.IDINSTPRN = :ID_PROCESSO
AND AD_ADVTRC.IDINSTTAR = :ID_TAREFA
) VLR_TRECHO,
(
SELECT NVL(SUM(AD_ADVTRCINT.VALOR), 0) VLR_TRECHO_INT
FROM AD_ADVTRCINT
WHERE AD_ADVTRCINT.IDINSTPRN = :ID_PROCESSO
AND AD_ADVTRCINT.IDINSTTAR = :ID_TAREFA
)VLR_TRECHO_INT